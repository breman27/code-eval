	private static void intersect(String line) {
		String[] lists = line.split(";");
		String[] l1 = lists[0].split("[\\W]");
		String[] l2 = lists[1].split("[\\W]");
		int smallest = Math.min(l1.length, l2.length);
		ArrayList<String> result = new ArrayList<String>();
		for (int i = 0; i < smallest; i++) {
			if(l1.length == smallest){
				if(Arrays.asList(l2).contains(l1[i])){
					result.add(l1[i]);
				}
			}
			else{
				if(Arrays.asList(l1).contains(l2[i])){
					result.add(l2[i]);
				}
			}
		}
		print(result);
	}

